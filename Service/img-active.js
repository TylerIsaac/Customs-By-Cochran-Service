function changeLift() {
  var image = document.getElementById('service-img');

  if (image.src.match("https://di-uploads-pod6.dealerinspire.com/1cochran/uploads/2022/05/LiftKit_Services.jpg")) {
      image.src = "https://di-uploads-pod6.dealerinspire.com/1cochran/uploads/2022/05/Default_Services.jpg";
  }

  else {
      image.src = "https://di-uploads-pod6.dealerinspire.com/1cochran/uploads/2022/05/LiftKit_Services.jpg";
  }
}

